// One-off build script: optimize the "סמינר פתיחה 25" photos into web-ready
// webp thumbnails + full-size images, and emit a typed manifest.
// Run: node scripts/optimize-seminar-gallery.cjs
const fs = require("fs");
const path = require("path");
const sharp = require("sharp");

const SRC_DIR = "C:/Users/roita/פורטל רשי/תמונות/סמינר פתיחה/סמינר פתיחה 25/תמונות";
const OUT_DIR = path.join(__dirname, "..", "public", "gallery", "seminar-2025");
const THUMB_DIR = path.join(OUT_DIR, "thumb");
const FULL_DIR = path.join(OUT_DIR, "full");
const MANIFEST = path.join(__dirname, "..", "src", "components", "about", "seminar-gallery.data.ts");

const THUMB_WIDTH = 1000; // slider cards (covers 2x DPR)
const FULL_WIDTH = 2000; // lightbox (covers retina / large screens)

for (const d of [THUMB_DIR, FULL_DIR]) fs.mkdirSync(d, { recursive: true });

// Collect source files and dedupe by base name. Windows leaves " (1)" copies;
// some photos only exist as a " (1)" file, so key on the base name (suffix
// stripped) and keep one per base, preferring the un-suffixed original.
const all = fs.readdirSync(SRC_DIR).filter((f) => /\.jpe?g$/i.test(f));
const byBase = new Map();
for (const f of all) {
  const ext = path.extname(f);
  const base = path.basename(f, ext).replace(/\s*\(\d+\)\s*$/, "").trim();
  const hasSuffix = /\(\d+\)\s*$/.test(path.basename(f, ext));
  const existing = byBase.get(base);
  if (!existing || (!hasSuffix && /\(\d+\)\s*$/.test(path.basename(existing, path.extname(existing))))) {
    byBase.set(base, f);
  }
}
const files = [...byBase.values()];

// Sort by the trailing sequence number (DSCxxxxx-NN.JPG) for a natural order.
const seq = (name) => {
  const m = name.match(/-(\d+)\.jpe?g$/i);
  return m ? parseInt(m[1], 10) : 0;
};
files.sort((a, b) => seq(a) - seq(b));

(async () => {
  const manifest = [];
  let i = 0;
  for (const file of files) {
    i += 1;
    const id = String(i).padStart(2, "0");
    const srcPath = path.join(SRC_DIR, file);

    const fullName = `${id}.webp`;
    const thumbName = `${id}.webp`;

    const fullInfo = await sharp(srcPath)
      .rotate() // respect EXIF orientation
      .resize({ width: FULL_WIDTH, withoutEnlargement: true })
      .webp({ quality: 80 })
      .toFile(path.join(FULL_DIR, fullName));

    await sharp(srcPath)
      .rotate()
      .resize({ width: THUMB_WIDTH, withoutEnlargement: true })
      .webp({ quality: 78 })
      .toFile(path.join(THUMB_DIR, thumbName));

    manifest.push({
      id,
      thumb: `/gallery/seminar-2025/thumb/${thumbName}`,
      full: `/gallery/seminar-2025/full/${fullName}`,
      width: fullInfo.width,
      height: fullInfo.height,
    });
    console.log(`  ${id}  ${file}  ->  ${fullInfo.width}x${fullInfo.height}`);
  }

  const ts = `// AUTO-GENERATED by scripts/optimize-seminar-gallery.cjs — do not edit by hand.
export type GalleryPhoto = {
  id: string;
  thumb: string;
  full: string;
  width: number;
  height: number;
};

export const seminarPhotos: GalleryPhoto[] = ${JSON.stringify(manifest, null, 2)};
`;
  fs.writeFileSync(MANIFEST, ts, "utf8");
  console.log(`\nWrote ${manifest.length} photos -> ${MANIFEST}`);
})();
